// Helper function to get optimized Cloudinary URL with transformations
export function getOptimizedImageUrl(url: string, options?: {
  width?: number;
  height?: number;
  quality?: 'auto' | 'low' | 'high';
  format?: 'auto' | 'webp' | 'jpg' | 'png';
}): string {
  if (!url.includes('cloudinary.com')) return url;
  
  const params = [];
  if (options?.width) params.push(`w_${options.width}`);
  if (options?.height) params.push(`h_${options.height}`);
  if (options?.quality) params.push(`q_${options.quality}`);
  if (options?.format) params.push(`f_${options.format}`);
  
  if (params.length === 0) return url;
  
  // Insert transformation parameters into the URL
  return url.replace('/image/upload/', `/image/upload/${params.join(',')}/`);
}
